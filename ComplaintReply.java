public class ComplaintReply extends Fragment {



ListView listView;

    @Override
    public View onCreateView(LayoutInflater inflater, ViewGroup container,
                             Bundle savedInstanceState) {
        // Inflate the layout for this fragment
        View view= inflater.inflate(R.layout.fragment_complaint_reply, container, false);


        listView=   view.findViewById(R.id.complaint_reply_list);


        getComplaintReply();

        return view;
    }

    public void getComplaintReply() {

        RequestQueue queue = Volley.newRequestQueue(getContext());
        StringRequest request = new StringRequest(Request.Method.POST, Utility.url, new Response.Listener<String>() {
            @Override
            public void onResponse(String response) {
                Log.d("******", response);
                if (!response.trim().equals("failed")) {

                    String data = response;
                    String arr[] = data.trim().split("#");

                    ArrayAdapter ar = new ArrayAdapter(getContext(),android.R.layout.simple_list_item_1,arr);
                    listView.setAdapter(ar);

                } else {
                    Toast.makeText(getContext(), "no data..!", Toast.LENGTH_LONG).show();
                }
            }
        }, new Response.ErrorListener() {
            @Override
            public void onErrorResponse(VolleyError error) {

                Toast.makeText(getContext(), "my error :" + error, Toast.LENGTH_LONG).show();
                Log.i("My error", "" + error);
            }
        }) {
            @Override
            protected Map<String, String> getParams() throws AuthFailureError {
                Map<String, String> map = new HashMap<String, String>();
                SharedPreferences prefs = getContext().getSharedPreferences("SharedData", MODE_PRIVATE);
                final String uid = prefs.getString("logid", "No logid");//"No name defined" is the default value.
                map.put("key","getComplaintReply");
                map.put("uid",uid);




                return map;
            }
        };
        queue.add(request);
    }

}
